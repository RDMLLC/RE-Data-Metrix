export const getToolsByFeatures = (selectedFeatures: (keyof ToolFeatures)[]): Tool[] => {
  if (selectedFeatures.length === 0) return tools;
  
  return tools.filter(tool => 
    selectedFeatures.every(feature => tool.features[feature])
  ).sort((a, b) => {
    const aCount = Object.values(a.features).filter(Boolean).length;
    const bCount = Object.values(b.features).filter(Boolean).length;
    return bCount - aCount;
  });
};

export const countMatchingFeatures = (tool: Tool, selectedFeatures: (keyof ToolFeatures)[]): number => {
  return selectedFeatures.filter(feature => tool.features[feature]).length;
};
